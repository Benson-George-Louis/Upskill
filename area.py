# This is a program to find the area of a rectangle 
length = float(input("Enter the Length of the rectangle: ")) # Asking the user the length of rectangle 
breadth = float(input("Enter the Breadth of the rectangle: ")) # Asking the user the Breadth of rectangle
area = length * breadth # Calculating the area
print(f"The area of rectangle = {area}") # Printing the output