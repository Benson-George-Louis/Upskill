# Accepting inputs from the user
a = int(input("Enter the first input:"))
b = float(input("Enter the second input:"))

# Displaying their types
print("Type of first input:", type(a))
print("Type of second input:", type(b))

# Type coversion and displaying
print("Type of first input after coversion", type(str(a)))
print("Type of second input after conversion", type(str(b)))