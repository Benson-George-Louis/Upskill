# Accepting a string from the user 
string = input("Enter a string: ")

# Converting sstring to upper case
print("Coverting string into uppercase: ", string.upper())

# Converting string to lower case
print("Conerting string into lowercase: ", string.lower())

# Replacing spaces with underscores
print("Replacing the Spaces: ", string.replace(" ", "_"))

# Checking whether it contains word "python"
print("Checking whether the string contains the word 'python': ", "python" in string.lower())